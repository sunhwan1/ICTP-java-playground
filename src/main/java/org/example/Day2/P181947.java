package org.example.Day2;
// 덧셈식 출력하기
import java.util.Scanner;

public class P181947 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        System.out.print(a);
        System.out.print(" + ");
        System.out.print(b);
        System.out.print(" = ");
        System.out.println(a + b);
    }
}